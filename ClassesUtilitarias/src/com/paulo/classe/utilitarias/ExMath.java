package com.paulo.classe.utilitarias;

public class ExMath {

	public static void main(String[] args) {
		 System.out.println(Math.pow(2, 3)); // 2 elevado 3
		 System.out.println(Math.round(4.7)); // Arrentonda
		 System.out.println(Math.round(4.4));
		 System.out.println(Math.ceil(4.4));// Arrentonda cima
		 System.out.println(Math.floor(4.4));// Arrentonda Baixo
		 
		 System.out.println(Math.round(Math.random() * 100)); // Gerar Aletorio
		 
		 System.out.println(Math.sin(Math.toRadians(30)));
		 System.out.println(Math.cos(Math.toRadians(1)));
		 System.out.println(Math.tan(Math.toRadians(45)));

	}

}
